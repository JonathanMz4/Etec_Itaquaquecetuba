
public class Principal {

    
    public static void main(String[] args) {
        int i;
        // Exibir número de 1 a 10
        for (i=1;i<=10;i++) {
            System.out.println(i);
        }
        // Exibir número de 10 a 1
        for (i=10;i>0;i--) {
            System.out.println(i);
        }
        // Exibir múltiplos de 5 no intervalo de 1 a 100
        for (i=0;i<=100;i+=5) {
            System.out.println(i);
        }
    }
    
}

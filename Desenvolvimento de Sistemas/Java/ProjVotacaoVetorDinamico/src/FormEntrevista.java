    private void buttonMediaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonMediaActionPerformed
        int soma = 0;
        for(int i=0;i<ent.notas.size();i++){
            soma+=Integer.parseInt(ent.notas.get(i));
        }
        if (ent.notas.size()>0) {
            ent.media=soma/ent.notas.size();
        } else {
            ent.media=0;
        }
        System.out.println("A media das notas e: "+ent.media);
    }//GEN-LAST:event_buttonMediaActionPerformed


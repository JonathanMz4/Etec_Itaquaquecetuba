    private void buttonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCadastrarActionPerformed
        if (this.txtNome.getText().equals("") || this.txtNota.getText().equals("") || Integer.parseInt(this.txtNota.getText())<0 || Integer.parseInt(this.txtNota.getText())>10){
            JOptionPane.showMessageDialog(null,"ERRO! Coloque seu nome corretamente e uma nota de 0 a 10");
        }
        else {
            ent.nomes.add(this.txtNome.getText());
            ent.notas.add(this.txtNota.getText());
            this.txtNome.setText("");
            this.txtNota.setText("");
        }
    }//GEN-LAST:event_buttonCadastrarActionPerformed


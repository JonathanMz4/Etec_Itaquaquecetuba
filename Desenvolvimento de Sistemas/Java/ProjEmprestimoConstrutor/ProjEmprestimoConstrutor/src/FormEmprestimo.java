    private void buttonSimularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimularActionPerformed
        emp.valor=Double.parseDouble(this.txtValor.getText());
        emp.numparcelas=Integer.parseInt(this.txtNparcelas.getText());
        double taxa=0;
        double nparcelas;
        double financiamento;
        double parcela;
        nparcelas = emp.numparcelas;
        if (nparcelas<=12){
            taxa = emp.valor * 0.05;
        }
        else {
            if (nparcelas<24){
                taxa = emp.valor * 0.10;
            }
            else {
                if (nparcelas>=24){
                    taxa = emp.valor * 0.20;
                }
            }
        }
        financiamento=nparcelas*taxa+emp.valor;
        parcela=financiamento/nparcelas;
        this.txtUlfinanciamento.setText(""+financiamento);
        this.txtVlparcela.setText(""+parcela);
        this.txtTaxa.setText(""+taxa);
        
    }//GEN-LAST:event_buttonSimularActionPerformed

